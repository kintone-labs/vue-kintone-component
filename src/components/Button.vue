<template>
    <button
        class="kuc-btn"
        v-if="isVisible"
        :class="className"
        :disabled="isDisabled"
        @click="handleClick"
    >
        {{ text }}
    </button>
</template>

<script lang="ts">
import Vue from 'vue';
export default Vue.extend({
    props: {
        text: {
            type: String,
            required: true,
        },
        type: {
            type: String,
            default: 'normal',
        },
        isDisabled: {
            type: Boolean,
            default: false,
        },
        isVisible: {
            type: Boolean,
            default: true,
        },
    },
    computed: {
        className() {
            const type = this.type;
            const valid =
                ['submit', 'normal'].indexOf(type) >= 0;
            return valid ? type : '';
        },
    },
    methods: {
        handleClick: function() {
            this.$emit('click');
        },
    },
});
</script>

